def validate():
	added = {}
	glyphs = homoglyphs.keys()
	res = True
	for glyph in glyphs:
		if (glyph != homoglyphs[glyph][0]):
			res = False
			print("first glyph has to be the same for " + glyph)
		for i in range(0, len(homoglyphs[glyph])):
			if (homoglyphs[glyph][i] in added):
				res = False
				print("duplicate found at " + str(i) + " of " + glyph)
			added[homoglyphs[glyph][i]] = True
	return res

def init():
	if (not validate()):
		raise "Invalid Homoglyph set"
	obj = {}
	glyphs = homoglyphs.keys()
	for glyph in glyphs:
		for homoglyph in homoglyphs[glyph]:
			obj[homoglyph] = homoglyphs[glyph]
	return obj

